package adHocDistanceVectorRouting;

public class RouteEntry {
	
	private String nextHop;
	private int age;
	
	public String getNextHop() {
		return nextHop;
	}
	public void setNextHop(String nextHop) {
		this.nextHop = nextHop;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
